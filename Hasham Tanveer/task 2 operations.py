# Arithmetic Operations
number1 = 29
number2 = 5
print("The sum of numbers is: ", number1+number2)
print("The multiplication of numbers is: ", number1*number2)
print("The subtract of numbers is: ", number1-number2)
print("The division of numbers is: ", number1/number2)
print("The remainder of numbers is: ", number1%number2)
print("The floor division of numbers is: ", number1//number2)
print("The exponential of numbers is: ", number1**number2)

#Logical Operations

check1  = number1==298
check2 = number2 == 59

print(check1 | check2 )
