marks = int(input("Please Enter Your marks:"))
if marks >= 90:
    print("Your grade is A+")
elif marks>= 80:
    print("Your grade is A")
elif marks>= 70:
    print("Your grade is B+")
elif marks>= 60:
    print("Your grade is B")
elif marks>= 50:
    print("Your grade is C")
else:
    print("You have failed the test.")
